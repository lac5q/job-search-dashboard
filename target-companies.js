// Helper function to get company match score
function getCompanyMatchScore(companyName) {
    if (!companyName) return 0;
    const normalized = companyName.toLowerCase().trim();

    // Direct match
    if (TARGET_COMPANIES[normalized]) {
        return TARGET_COMPANIES[normalized].score;
    }

    // Partial match - check if company name contains or is contained by a key
    for (const [key, data] of Object.entries(TARGET_COMPANIES)) {
        if (normalized.includes(key) || key.includes(normalized)) {
            return data.score;
        }
    }

    return 0;
}

// Helper function to get full company info
function getCompanyInfo(companyName) {
    if (!companyName) return null;
    const normalized = companyName.toLowerCase().trim();

    // Direct match
    if (TARGET_COMPANIES[normalized]) {
        return { ...TARGET_COMPANIES[normalized], name: companyName };
    }

    // Partial match
    for (const [key, data] of Object.entries(TARGET_COMPANIES)) {
        if (normalized.includes(key) || key.includes(normalized)) {
            return { ...data, name: companyName, matchedKey: key };
        }
    }

    return null;
}
